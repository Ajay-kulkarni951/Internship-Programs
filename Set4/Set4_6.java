/*WAP to illustrate the use of left shift and right shift operators*/
package set4_6;


public class Set4_6 {

    
    public static void main(String[] args) {
        int a = 5, b = 4;
        int c = a>>1;
        System.out.println(c);
        int d = b<<1;
        System.out.println(d);
    }
    
}
