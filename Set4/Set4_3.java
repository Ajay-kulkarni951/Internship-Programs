/*WAP to Illustrate the use of Relational operators */
package set4_3;


public class Set4_3 {

    
    public static void main(String[] args) {
        int a = 5, b = 3, c = 8, d = 10;
        System.out.println(a>b);
        System.out.println(b<c);
        System.out.println(a>=b);
        System.out.println(d<=c);
        System.out.println(a!=b);
        System.out.println(b==c);
    }
    
}
