/*WAP to illustrate the use of bitwise and ,or and XOR operators*/
package set4_5;


public class Set4_5 {

    
    public static void main(String[] args) {
        int a = 5, b = 4;
        int c = a&b;
        int d = a|b;
        int e = a^b;
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
    
}
