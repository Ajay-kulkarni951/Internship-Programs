/*. WAP to illustrate the use of shortcut of assignment operator for all arithmetic operations*/
package set4_7;


public class Set4_7 {

   
    public static void main(String[] args) {
        int a = 10;
        a += 4;
        System.out.println(a);
        a -= 8;
        System.out.println(a);
        a *= 2;
        System.out.println(a);
        a /= 10;
        System.out.println(a);
    }
    
}
