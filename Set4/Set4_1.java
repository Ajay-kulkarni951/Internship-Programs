/*WAP to illustrate the use of post and pre increment operator and Decrement Operator*/
package set4_1;


public class Set4_1 {

    
    public static void main(String[] args) {
         int a=5, b=9,c=3;
        System.out.println(a++);
        int x = b++;
        System.out.println(x);
        System.out.println(a);
        System.out.println(b);
        System.out.println(++c);  
    }
    
}
